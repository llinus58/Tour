    public void cbLoaiHinhDuLich() {

        ArrayList<LoaiHinhDuLichDTO> list = new LoaiHinhDuLichBLL().getAll();
        for (LoaiHinhDuLichDTO dl : list) {
            cbLoaiHinhDuLich.addItem(dl.getTenLoaiDuLich());
        }
        cbLoaiHinhDuLich.setSelectedIndex(0);
    }

    public void LocListTour() {
        modeltour.setRowCount(0);
        listtour = new TourDAL().getList();
        for (TourDTO t : listtour) {
            modeltour.addRow(new Object[]{t.getMaTour(), t.getTenTour(), new LoaiHinhDuLichBLL().goitenLoaiHinhDuLich(t.getMaLoaiHinhDuLich())});
        }
        this.rowSorter = TableSetupUtil.setTableFilter(tblTour, txtTimKiem);
//        String test = new TourDAL().GoiTenLoaiHinhDuLich("1");
//        System.out.println("kq : " + test);
    }

    public void ResetTxt() {
        txtMaTour.setText("");
        cbLoaiHinhDuLich.setSelectedItem(null);
        txtTenTour.setText("");

        LocListTour();
    }

