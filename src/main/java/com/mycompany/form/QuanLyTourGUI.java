    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        if (txtMaTour.getText().isEmpty()) {
            if (txtTenTour.getText().isEmpty() || cbLoaiHinhDuLich.getSelectedItem() == null) {
                JOptionPane.showMessageDialog(rootPane, "Chua nhap du thong tin");
            }
         else if(!InputValidatorUtil.isValidName(txtTenTour.getText(), true).isEmpty()){
             JOptionPane.showMessageDialog(rootPane, "Ten tour khong hop le");
        }
            
            else {
                TourDTO dv = new TourDTO();
                
                dv.setMaLoaiHinhDuLich(new LoaiHinhDuLichBLL().goimaLoaiHinhDuLich(String.valueOf(cbLoaiHinhDuLich.getSelectedItem())));
                dv.setTenTour(txtTenTour.getText());
                if (new TourBLL().themTour(dv)) {
                    JOptionPane.showMessageDialog(rootPane, "Da them");
                    ResetTxt();
                    LocListTour();
                } else {
                    //JOptionPane.showMessageDialog(rootPane,)
                    JOptionPane.showMessageDialog(rootPane, cbLoaiHinhDuLich.getSelectedItem());
                    JOptionPane.showMessageDialog(rootPane, "ERROR");
                }
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Trùng mã");
            ResetTxt();
        }
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        int i = tblTour.getSelectedRow();
        if (i < 0) {
            JOptionPane.showMessageDialog(rootPane, "Chon dong can thuc hien");
        } else {
            if (txtMaTour.getText().isEmpty() || txtTenTour.getText().isEmpty() || cbLoaiHinhDuLich.getSelectedItem() == null) {
                JOptionPane.showMessageDialog(rootPane, "Chua nhap du thong tin");
            } 
              else if(!InputValidatorUtil.isValidName(txtTenTour.getText(), true).isEmpty()){
             JOptionPane.showMessageDialog(rootPane, "Ten tour khong hop le");
        }
            
            else {

                //DichVuDTO dv = new DichVuDTO();
                int MaTour = Integer.parseInt(String.valueOf(txtMaTour.getText()));
                String TenTour = txtTenTour.getText();
                String TenLoaiDL = cbLoaiHinhDuLich.getSelectedItem().toString();
                int MaLoaiDL = new LoaiHinhDuLichBLL().goimaLoaiHinhDuLich(TenLoaiDL);
                int conf = JOptionPane.showConfirmDialog(rootPane, "Bạn có chắc chắn muốn sửa TOUR này ?", "Confirm", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
                if (conf == JOptionPane.YES_OPTION) {
                    if (new TourBLL().suaTour(MaTour, TenTour, MaLoaiDL)) {
                        JOptionPane.showMessageDialog(rootPane, "Sua thanh cong");
                        ResetTxt();
                        LocListTour();
                    } else {
                        JOptionPane.showMessageDialog(rootPane, "ERROR");
                    }
                }
            }
        }
    }//GEN-LAST:event_btnSuaActionPerformed

