    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
       int i = tblDiaDiem.getSelectedRow();
        if (i < 0) {
            JOptionPane.showMessageDialog(rootPane, "Hay chon dong can sua !");

        } else if (txtTenDiaDiem.getText().isBlank() || txtDiaChi.getText().isBlank() || txtGioiThieu.getText().isBlank()) {
            JOptionPane.showMessageDialog(rootPane, "Hay nhap day du thong tin !");
        }  else if (!InputValidatorUtil.isValidName(txtTenDiaDiem.getText(), true).isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Ten dia diem khong hop le");
        } else if (!InputValidatorUtil.isValidAddress(txtDiaChi.getText()).isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Dia chi khong khong hop le");
        } else if (!InputValidatorUtil.isValidAddress(txtGioiThieu.getText()).isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Gioi thieu khong khong hop le");
        } else {
            DiaDiemDTO d = new DiaDiemDTO();
            d.setMaDiaDiem(Integer.parseInt(txtMaDiaDiem.getText()));
            d.setMaTinh(Integer.parseInt(String.valueOf(cbTinh.getSelectedItem()).split(":")[0]));
            d.setTenDiaDiem(txtTenDiaDiem.getText());
            d.setDiaChi(txtDiaChi.getText());
            d.setGioiThieu(txtGioiThieu.getText());
            ddb.update(d);
            JOptionPane.showMessageDialog(rootPane, "Da sua dia diem");
            loadTableData();
            resetForm();
        }
    }//GEN-LAST:event_btnSuaActionPerformed

