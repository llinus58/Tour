    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
       if(!(tblDiaDiem.getSelectedRow()<0)){
           int row = tblDiaDiem.getSelectedRow();
           int ma = Integer.parseInt(String.valueOf(tblDiaDiem.getValueAt(row, 0)));
            int conf = JOptionPane.showConfirmDialog(rootPane, "Thao tac se xoa dia diem nay khoi tat ca Tour neu co ?", "Confirm", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
            if (conf == JOptionPane.YES_OPTION) {
               ddb.delete(ma);  //xoa dia diem
               ddb.delete_dsdd(ma); //xoa khoi dsdd_tour
               JOptionPane.showMessageDialog(rootPane, "Xoa thanh cong");
               loadTableData();
               resetForm();
            }
       }else{
           JOptionPane.showMessageDialog(rootPane, "Hay chon dia diem can xoa !");
       }
    }//GEN-LAST:event_btnXoaActionPerformed
