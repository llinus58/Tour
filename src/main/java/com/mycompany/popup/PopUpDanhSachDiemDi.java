    private void btnThemMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnThemMousePressed
        // TODO add your handling code here:
        String diadiem = String.valueOf(cbDanhSachDiemDi.getSelectedItem());
        if (diadiem.equals("---Chọn Địa Điểm---")) {
            JOptionPane.showMessageDialog(rootPane, "Vui lòng chọn địa điểm cần THÊM...");
        } else {
            ArrayList<String> list = new DS_DiaDiemTourBLL().getDSDiaDiem(QuanLyTourGUI.matour);
            int dem = 0;
            for (int i = 0; i < list.size(); i++) {
                if (diadiem.equals(list.get(i))) {
                    dem++;
                }
            }
            if(dem != 0)
            {
                JOptionPane.showMessageDialog(rootPane, "Địa điểm này hiện ĐÃ có trong danh sách địa điểm ...");
            }
            else{
                int madiadiem = new DS_DiaDiemTourBLL().timMaDiaDiem(diadiem);
                if(new DS_DiaDiemTourBLL().themDS_DiaDiemTour(Integer.parseInt(lbMaTour.getText()), madiadiem)){
                    JOptionPane.showMessageDialog(rootPane, "Thêm thành công !");
                    setTblDSDD();
                    setCBdiadiem();
                }
                else{
                    JOptionPane.showMessageDialog(rootPane, "Thêm thất bại !");
                }
                
            }
        }

    }//GEN-LAST:event_btnThemMousePressed

    private void btnXoaMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnXoaMousePressed
        // TODO add your handling code here:
        int i = tblDanhSachDiemDi.getSelectedRow();
        if (i < 0) {
            JOptionPane.showMessageDialog(rootPane, "Chon dong can thuc hien");
        } else {
            int MaTour = Integer.parseInt(lbMaTour.getText());
            //int MaDiaDiem = Integer.parseInt()  ;
            int MaDiaDiem = new DS_DiaDiemTourBLL().timMaDiaDiem(String.valueOf(modeldsdd.getValueAt(i, 1)));
            
            
            int conf = JOptionPane.showConfirmDialog(rootPane, "Bạn có chắc chắn muốn xóa TOUR này ?", "Confirm", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
            if (conf == JOptionPane.YES_OPTION) {
                if (new DS_DiaDiemTourBLL().xoaDS_DiaDiemTour(MaTour,MaDiaDiem)) {
                    JOptionPane.showMessageDialog(rootPane, "Xoa thanh cong");
                    setTblDSDD();
                    setCBdiadiem();
                    
                } else {
                    JOptionPane.showMessageDialog(rootPane, "ERROR");
                }
            }
        }
    }//GEN-LAST:event_btnXoaMousePressed

