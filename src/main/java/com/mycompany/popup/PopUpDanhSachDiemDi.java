    public void setTblDSDD() {

        modeldsdd.setRowCount(0);

        //btnThemActionPerformed(java.awt.event.ActionEvent evt);
//        System.out.println(tour.matour);
//        System.out.println(lbLoaiHinhDuLich.getText());
//        System.out.println(lbTenTour.getText());
    
        ArrayList<String> list = new DS_DiaDiemTourBLL().getDSDiaDiem(QuanLyTourGUI.matour);

        for (int i = 0; i < list.size(); i++) {
            modeldsdd.addRow(new Object[]{i + 1, list.get(i)});
            //System.out.println((i + 1) + " /  " + list.get(i));
        }
        
        
    }

    public void setCBdiadiem() {
        List<DiaDiemDTO> list = new DiaDiemBLL().findAll();
        cbDanhSachDiemDi.addItem("---Chọn Địa Điểm---");

        for (DiaDiemDTO dd : list) {

            cbDanhSachDiemDi.addItem(dd.getTenDiaDiem());
        }
        
        cbDanhSachDiemDi.setSelectedIndex(0);
    }
