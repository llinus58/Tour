    private void btnThemNhanVienActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemNhanVienActionPerformed
        if(tblDSNhanVien.getSelectedRow()<0){
            JOptionPane.showMessageDialog(rootPane, "Hay chon nhan vien can them !");
        }else{
            int i = tblDSNhanVien.getSelectedRow();
            int idkhach = Integer.parseInt(String.valueOf(tblDSNhanVien.getValueAt(i, 0)));
            int iddoan = QuanLyDoanGUI.idDoan;
            if(db.themNVvaoDoan(idkhach, iddoan)){
                JOptionPane.showMessageDialog(rootPane, "Da them nhan vien!");
                loadTableNV_Cothethem();
                loadTableNV();
            }else{
                 JOptionPane.showMessageDialog(rootPane, "Co loi xay ra");
            }
        }
    }//GEN-LAST:event_btnThemNhanVienActionPerformed
    public void loadTableNV_Cothethem(){
        listNVCTT = db.listNV_Doan_Cothethem(QuanLyDoanGUI.idDoan);
        dtm2 = (DefaultTableModel) tblDSNhanVien.getModel();
        dtm2.setRowCount(0);
        for(NhanVienDTO nv:listNVCTT){
            dtm2.addRow(new Object[]{nv.getMaNhanVien(),nv.getHoTen(),nv.getGioiTinh()? "Nữ" : "Nam",nv.getSDT(),vb.findById(nv.getMaVaiTro()).getTenVaiTro()});
        }
       this.rowSorter = TableSetupUtil.setTableFilter(tblDSNhanVien, txtTimKiem);
    }
