    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
       if(tblDSNhanVienDaThem.getSelectedRow()<0){
            JOptionPane.showMessageDialog(rootPane, "Hay chon nhan vien can xoa !");
        }else{
            int i = tblDSNhanVienDaThem.getSelectedRow();
            int idnv = Integer.parseInt(String.valueOf(tblDSNhanVienDaThem.getValueAt(i, 0)));
            int iddoan = QuanLyDoanGUI.idDoan;
            if(db.xoaNVkhoiDoan(idnv, iddoan)){
                JOptionPane.showMessageDialog(rootPane, "Da xoa nhan vien ");
                
                loadTableNV();
            }else{
                 JOptionPane.showMessageDialog(rootPane, "Co loi xay ra");
            }
        }
    }//GEN-LAST:event_btnXoaActionPerformed
