    public void setupForm(){
        DoanDTO d = db.findDoanbyID(QuanLyDoanGUI.idDoan);      
        txtTenDoan.setText(d.getTenDoan());
        loadTableKH();
        loadTableNV();
    }
    public void loadTableKH(){
        listKH = db.listKH_Doan(QuanLyDoanGUI.idDoan);
        dtm = (DefaultTableModel) tblKhachDoan.getModel();
        dtm.setRowCount(0);
        for(KhachHangDTO kh:listKH){
            dtm.addRow(new Object[]{kh.getMaKH(),kh.getHoTen(),kh.getSDT()});
        }
       
    }
    public void loadTableNV(){
        listNV = db.listNV_Doan(QuanLyDoanGUI.idDoan);
        dtm = (DefaultTableModel) tblNhanVienDoan.getModel();
        dtm.setRowCount(0);
        for(NhanVienDTO nv:listNV){
            dtm.addRow(new Object[]{nv.getMaNhanVien(),nv.getHoTen(),vb.findById(nv.getMaVaiTro()).getTenVaiTro()});
        }
       
    }
