    private void btnXoaKhachDoanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaKhachDoanActionPerformed
        if(tblKhachDoan.getSelectedRow()<0){
            JOptionPane.showMessageDialog(rootPane, "Hay chon khach hang can xoa !");
        }else{
            int i = tblKhachDoan.getSelectedRow();
            int idkhach = Integer.parseInt(String.valueOf(tblKhachDoan.getValueAt(i, 0)));
            int iddoan = QuanLyDoanGUI.idDoan;
            if(db.xoaKHkhoiDoan(idkhach, iddoan)){
                JOptionPane.showMessageDialog(rootPane, "Da xoa khach hang!");
                
                loadTableKH();
            }else{
                 JOptionPane.showMessageDialog(rootPane, "Co loi xay ra");
            }
        }
    }//GEN-LAST:event_btnXoaKhachDoanActionPerformed

    private void btnXoaNVDoanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaNVDoanActionPerformed
        if(tblNhanVienDoan.getSelectedRow()<0){
            JOptionPane.showMessageDialog(rootPane, "Hay chon nhan vien can xoa !");
        }else{
            int i = tblNhanVienDoan.getSelectedRow();
            int idnv = Integer.parseInt(String.valueOf(tblNhanVienDoan.getValueAt(i, 0)));
            int iddoan = QuanLyDoanGUI.idDoan;
            if(db.xoaNVkhoiDoan(idnv, iddoan)){
                JOptionPane.showMessageDialog(rootPane, "Da xoa nhan vien ");
                
                loadTableNV();
            }else{
                 JOptionPane.showMessageDialog(rootPane, "Co loi xay ra");
            }
        }
    }//GEN-LAST:event_btnXoaNVDoanActionPerformed

