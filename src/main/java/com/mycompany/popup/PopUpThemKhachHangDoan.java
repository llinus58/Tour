    private void btnThemKhachDoanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemKhachDoanActionPerformed
        if(tblKhachHang.getSelectedRow()<0){
            JOptionPane.showMessageDialog(rootPane, "Hay chon khach hang can them !");
        }else{
            int i = tblKhachHang.getSelectedRow();
            int idkhach = Integer.parseInt(String.valueOf(tblKhachHang.getValueAt(i, 0)));
            int iddoan = QuanLyDoanGUI.idDoan;
            if(db.themKHvaoDoan(idkhach, iddoan)){
                JOptionPane.showMessageDialog(rootPane, "Da them khach hang!");
                loadTableKH_Cothethem();
                loadTableKH();
            }else{
                 JOptionPane.showMessageDialog(rootPane, "Co loi xay ra");
            }
        }
    }//GEN-LAST:event_btnThemKhachDoanActionPerformed

