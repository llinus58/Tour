    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
       if(tblKhachDaThem.getSelectedRow()<0){
            JOptionPane.showMessageDialog(rootPane, "Hay chon khach hang can xoa !");
        }else{
            int i = tblKhachDaThem.getSelectedRow();
            int idkhach = Integer.parseInt(String.valueOf(tblKhachDaThem.getValueAt(i, 0)));
            int iddoan = QuanLyDoanGUI.idDoan;
            if(db.xoaKHkhoiDoan(idkhach, iddoan)){
                JOptionPane.showMessageDialog(rootPane, "Da xoa khach hang!");
                loadTableKH_Cothethem();
                loadTableKH();
            }else{
                 JOptionPane.showMessageDialog(rootPane, "Co loi xay ra");
            }
        }
    }//GEN-LAST:event_btnXoaActionPerformed
    public void setForm(){
        DoanDTO d = db.findDoanbyID(QuanLyDoanGUI.idDoan);
        lbMaDoan.setText(String.valueOf(d.getId()));
        lbTenDoan.setText(d.getTenDoan());
    }
    public void loadTableKH(){
        listKH = db.listKH_Doan(QuanLyDoanGUI.idDoan);
        dtm = (DefaultTableModel) tblKhachDaThem.getModel();
        dtm.setRowCount(0);
        for(KhachHangDTO kh:listKH){
            dtm.addRow(new Object[]{kh.getMaKH(),kh.getHoTen(),kh.getSDT()});
        }
       
    }
    public void loadTableKH_Cothethem(){
        listKHCTT = db.listKH_Doan_Cothethem(QuanLyDoanGUI.idDoan);
        dtm2 = (DefaultTableModel) tblKhachHang.getModel();
        dtm2.setRowCount(0);
        for(KhachHangDTO kh:listKHCTT){
            dtm2.addRow(new Object[]{kh.getMaKH(),kh.getHoTen(),kh.getGioiTinh()? "Nữ" : "Nam",kh.getSDT(),kh.getCMND()});
        }
       this.rowSorter = TableSetupUtil.setTableFilter(tblKhachHang, txtTimKiem);
    }
