    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        if(txtGiaTour.getText().isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Hay nhap gia co ban !");
        }else{
            GoiTourDTO dt = gb.timGoiTour(QuanLyGoiTourGUI.idgiatour);
        if(gb.xoaDSDV(dt.getMaGiaTour())){
            if(gb.thaydoiGiaCoBan(dt.getMaGiaTour(), Double.parseDouble(txtGiaTour.getText()))){
                ArrayList<Integer> chondv = new ArrayList<>();
        for (int i = 0; i < tblThongTinGoi.getRowCount(); i++) {
            if ((Boolean) modeldichvu.getValueAt(i, 3)) {
                chondv.add(Integer.parseInt(String.valueOf(modeldichvu.getValueAt(i, 0))));
            }
        }
        
            gb.themDSDV_Tour(dt.getMaGiaTour(), chondv);
                JOptionPane.showMessageDialog(rootPane, "Da luu thay doi");
                dispose();
        
            }else{
                JOptionPane.showMessageDialog(rootPane,"Thay doi gia co ban that bai");
            }
        }else{
            JOptionPane.showMessageDialog(rootPane,"Xoa danhsach dich vu that bai");
        }
        }
        
    }//GEN-LAST:event_btnLuuActionPerformed

