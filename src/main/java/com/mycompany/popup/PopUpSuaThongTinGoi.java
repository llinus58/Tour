    public void TinhGiaTongDV() {
        double tong;
        if(txtGiaTour.getText().isBlank()){
            tong=0;
        }
        else{
            tong=Double.parseDouble(txtGiaTour.getText());
        }
        
            for (int i = 0; i < tblThongTinGoi.getRowCount(); i++) {
                if ((Boolean) modeldichvu.getValueAt(i, 3)) {
                    tong += Double.parseDouble(String.valueOf(modeldichvu.getValueAt(i, 2)).replace(",", ""));
                } else {
                    tong += 0;
                }
            }
            txtTong.setText(String.valueOf(formatter.format(tong)));
        
        
    }
    public void setUp() {
        GoiTourDTO dt = gb.timGoiTour(QuanLyGoiTourGUI.idgiatour);
        txtTenTour.setText(gb.GoiTenTour(dt.getMaTour()));
        txtGiaTour.setText(String.valueOf((long)dt.getGiaCoBan()));
        txtTong.setText(String.valueOf(formatter.format((long)(dt.getGiaCoBan()+gb.TongGiaDV(dt.getMaGiaTour())))));
    }

    public void LocListDV() throws SQLException {
        modeldichvu.setRowCount(0);
        listdv = dv.getAll("1", "MaDichVu ASC");
        ArrayList<Integer> dsdv = gb.layDSDV(QuanLyGoiTourGUI.idgiatour);
        for (DichVuDTO dv : listdv) {
            modeldichvu.addRow(new Object[]{dv.getMaDV(), dv.getTenDV(), String.valueOf(formatter.format(dv.getGiaDV())), kiemtratontai(dv.getMaDV(), dsdv)});
        }
    }
    public boolean kiemtratontai(int iddv, ArrayList<Integer> dsdv) {
        for (int a : dsdv) {
            if (a == iddv) {
                return true;         
            }

        }
        return false;
    }

