    public void fillCbTour() {
        cbTour.removeAllItems();
        listcbTour = db.fillCBTour();
        for (String s : listcbTour) {
            cbTour.addItem(s);
           
        }
    }

    public void LocListDV() throws SQLException {
        modeldichvu.setRowCount(0);
        listdv = dv.getAll("1", "MaDichVu ASC");
        for (DichVuDTO dv : listdv) {
            modeldichvu.addRow(new Object[]{dv.getMaDV(), dv.getTenDV(), String.valueOf(formatter.format(dv.getGiaDV())), false});
        }
    }

