    public void TinhGiaTongDV() {
        double tong;
        if(txtGiaTourCoBan.getText().isBlank()){
            tong=0;
        }
        else{
            tong=Double.parseDouble(txtGiaTourCoBan.getText());
        }
        
            for (int i = 0; i < tblDichVuCoSan.getRowCount(); i++) {
                if ((Boolean) modeldichvu.getValueAt(i, 3)) {
                    tong += Double.parseDouble(String.valueOf(modeldichvu.getValueAt(i, 2)).replace(",", ""));
                } else {
                    tong += 0;
                }
            }
            txtTongChiPhi.setText(String.valueOf(formatter.format(tong)));
        
    }
    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        if(txtGiaTourCoBan.getText().isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Hay nhap gia co ban !");
        }else{
            chondv = new ArrayList<>();
        for (int i = 0; i < tblDichVuCoSan.getRowCount(); i++) {
            if ((Boolean) modeldichvu.getValueAt(i, 3)) {
                chondv.add(Integer.parseInt(String.valueOf(modeldichvu.getValueAt(i, 0))));
            }
        }
        double giacoban = Double.parseDouble(txtGiaTourCoBan.getText());
        int matour = Integer.parseInt(String.valueOf(cbTour.getSelectedItem()).split(":")[0]);
        if(gb.themGoiTour(matour, giacoban)){
            int mavuatao = gb.magiatourVuathem();
            gb.themDSDV_Tour(mavuatao, chondv);
                JOptionPane.showMessageDialog(rootPane, "Da them mot goi gia Tour moi");
                dispose();
        }
        }
        
    }//GEN-LAST:event_btnThemActionPerformed

